const numero = 10;

if (numero >= 0 && numero <= 5){
    console.log("Número entre 0 e 5");

}else if (numero >= 6 && numero <= 8){
    console.log("Numero entre 6 e 8");

}else if (numero >= 9 && numero <= 11){
    console.log("Número entre 9 e 11");

}else {
    console.log("Número não está entre 0 e 11");
}